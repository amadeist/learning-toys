var_1 = input("Enter first variable: ")
var_2 = input("Enter second variable: ")

print("Before swapping first-variable = {0} and second-variable = {1}".format(var_1,var_2))

print("Variables swapping...")

temp = var_1
var_1 = var_2
var_2 = temp

print("After swapping first-variable = {0} and second-variable = {1}".format(var_1,var_2))

